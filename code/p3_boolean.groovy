// Shows the primitive boolean type in groovy as well as boolean operators: and
// or, and not.
class p3_boolean{
	// Shows off the boolean type by implementing the xor operation between
	// two booleans a and b.
	static void main(String[] args){
		boolean a = true
		boolean b = false
		println ((!a && b) || (a && !b))
	}

} 
